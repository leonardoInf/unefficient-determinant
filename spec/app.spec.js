const det = require("../determinant.js");

describe("The determinant should be correct when it", function () {
  it("uses a two-dimensional matrix", () => {
    expect(
      det([
        [0, 1],
        [2, 3],
      ])
    ).toEqual(-2);
  });
  it("uses a three-dimensional matrix", () => {
    expect(
      det([
        [0, 1, 5],
        [2, 3, 0],
        [4, 0, 2],
      ])
    ).toEqual(-64);
  });
  it("uses a four-dimensional matrix", () => {
    expect(
      det([
        [0, 1, 5, 1],
        [2, 3, 0, 0.3],
        [4, 0, 2, 2],
        [1, 1, 1, 2],
      ])
    ).toEqual(-98.6);
  });
  it("uses a five-dimensional matrix", () => {
    expect(
      det([
        [10, -12, -3, -15, -6],
        [-4, -20, 3, 9, 0],
        [10, -9, -3, -14, -2],
        [-11, -17, -18, -11, -19],
        [15, -16, -17, 11, 11],
      ])
    ).toEqual(823384);
  });
  it("uses a high-dimensional matrix", () => {
    expect(
      det([
        [-10, 20, 0, 13, 3, -19, -3, -16, -18, 2, -18],
        [-2, -16, 20, -11, -19, 15, -12, 18, -18, 8, -20],
        [-18, -6, -18, 17, 10, 3, 16, 20, -2, -8, -2],
        [-16, 1, 7, 5, 15, -14, -4, 10, 2, -12, -20],
        [16, 4, -1, -14, -8, -6, -10, 19, -3, -12, 1],
        [-14, -3, 19, 9, 20, -20, -6, 12, 3, 9, -3],
        [17, 0, 15, 12, -17, -10, 10, 13, 7, -1, -9],
        [-13, 6, 2, -11, 3, 2, -3, 14, -11, -3, -5],
        [18, 6, -15, 17, 12, -13, 4, 19, -14, -1, -7],
        [-8, -10, 1, 4, 8, 1, -12, 0, 12, -6, -8],
        [18, -15, 0, -6, 15, -12, 5, 0, 0, -7, -12],
      ])
    ).toEqual(-748910661537505);
  });
});
